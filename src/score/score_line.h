#ifndef SCORELINE_H
#define SCORELINE_H

#include <string>
#include <vector>
#include <iosfwd>


class ScoreLine
{

public:
    ScoreLine();

    bool operator < ( const ScoreLine& sl ) const;

    // equality of scorelines is defined as equal timestamp, message, and intArgs[ 0 ]
    // ( which is most often the sourceId )
    bool operator == ( const ScoreLine& sl ) const;
    
    friend std::ostream& operator << ( std::ostream& out, const ScoreLine& scoreline );

    float       timestamp;
    std::string method;

    std::vector< int >         intArgs;
    std::vector< float >       floatArgs;
    std::vector< std::string > stringArgs;
};    

#endif
